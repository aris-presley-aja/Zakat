			Mengutip penjelasan ahli tafsir Quraish Shihab dalam buku Tafsir Al Misbah, zakat berarti penyucian dan berkembang. Maksudnya, melalui pembayaran zakat, seorang Muslim diajak untuk menyucikan jiwa dengan mengikis sifat tamak, kikir, loba di dalam dirinya.<br><br>
